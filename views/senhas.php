
      <h1>Senhas Seguras</h1>
      <p>
        Senhas são como chaves que protegem nossas contas e informações pessoais. Se alguém descobre sua senha, pode acessar seus e-mails, redes sociais, bancos e muito mais. Por isso, é fundamental criar senhas que sejam difíceis de adivinhar, até mesmo por pessoas que conhecem você.
      </p>

      <h3>A prática mais recomendada:</h3>
      <p>Usar uma combinação de letras maiúsculas e minúsculas, números e símbolos.
        Exemplo: J0@n4#2025!
      </p>

      <p>
        Essa combinação torna a senha mais complexa e segura, dificultando o trabalho de hackers e programas que tentam adivinhar senhas automaticamente.
      </p>

      <h3>O que evitar:</h3>
      <ul>
        <li>Não use a mesma senha em vários sites. Se um deles for invadido, os outros ficam vulneráveis.</li>
        <li>Evite usar dados pessoais, como seu nome, data de nascimento ou nome de pets. Isso pode ser facilmente descoberto por quem te conhece ou pelas redes sociais.</li>
        <li>Não use palavras comuns como "senha", "123456", "admin", ou "qwerty". Elas são as primeiras que os hackers tentam.</li>
      </ul>

        <h3>Dica extra:</h3>
      <p>
        Use gerenciadores de senha para criar e armazenar senhas seguras — eles ajudam a manter tudo protegido sem você precisar decorar cada uma.
      </p>

