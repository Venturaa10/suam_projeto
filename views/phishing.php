

      <h2>O que é Phishing?</h2>
      <p>
        Phishing é uma técnica de fraude online usada por cibercriminosos para enganar as pessoas e fazer com que elas revelem informações confidenciais, como:
      </p>
      <ul>
        <li>Senhas</li>
        <li>Números de cartão de crédito</li>
        <li>Dados bancários</li>
        <li>Informações pessoais</li>
      </ul>

      <p>
        O golpe normalmente acontece por meio de:
      </p>
      <ul>
        <li>E-mails falsos que imitam bancos ou empresas conhecidas</li>
        <li>Mensagens de texto ou WhatsApp</li>
        <li>Sites falsos que parecem reais</li>
      </ul>

      <h3>Phishing nas redes sociais</h3>
      <p>
        As redes sociais são alvos frequentes desses golpes. Você pode receber:
      </p>
      <ul>
        <li>Um link que promete algo chamativo ("Veja quem viu seu perfil!", "Ganhe um cupom!")</li>
        <li>Uma mensagem com alerta falso ("Sua conta foi bloqueada, clique aqui para resolver.")</li>
        <li>Perfis falsos se passando por amigos ou empresas</li>
      </ul>

      <p>
        Essas ações podem levar ao roubo de identidade, onde o criminoso se faz passar por você para aplicar golpes em outras pessoas ou acessar sua vida digital.
      </p>

      <h3>Como se proteger do phishing:</h3>
      <ul>
        <li>Desconfie de links suspeitos, mesmo que venham de amigos.</li>
        <li>Não clique em mensagens que pedem seus dados, especialmente se parecerem urgentes.</li>
        <li>Verifique o endereço do site: Golpistas usam URLs parecidas com as verdadeiras (ex: g00gle.com em vez de google.com).</li>
        <li>Ative a verificação em duas etapas nas suas contas.</li>
        <li>Nunca informe senhas ou dados bancários por e-mail, mensagem ou sites que não tenham "https://" e cadeado de segurança.</li>
      </ul>

      <h3>Exemplo real:</h3>
      <p>
        Você recebe um e-mail dizendo:

      "Sua conta no Banco X foi bloqueada. Clique aqui para resolver."

        Esse link leva a um site falso, onde você digita seus dados achando que está resolvendo um problema, mas na verdade está entregando tudo ao golpista.
      </p>

