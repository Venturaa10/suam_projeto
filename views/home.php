      <h2>Bem-vindo ao nosso site!</h2>
      <p>Este site é dedicado a ajudar pessoas leigas a entenderem os principais riscos ao navegar na internet e como se proteger de forma simples e eficaz.</p>

      <h2>O que é segurança na internet?</h2>
      <p>Podemos entender como Segurança na Internet todas os cuidados que devemos ter para proteger as coisas que fazem parte da internet como a infraestrutura, que podem ser nossos computadores e as informações, que são as mais atacadas pelos cibercriminosos.</p>

      <h2>Os principais riscos da Internet</h2>
      <p>Algumas das coisas que os cibercriminosos tentam fazer pela Internet são:</p>
      <ul>
        <li>Roubar informações</li>
        <li>Corromper informações</li>   
        <li>Atacar sistemas ou equipamentos </li> 
        <li>Roubar identidade</li>
        <li>Vender dados pessoais </li>  
        <li>Roubar dinheiro</li>
      </ul>

      <h2>Como evitar?</h2>
      <p>Se alguém lida com muitas informações e possui vários equipamentos, como no caso das empresas, é melhor solicitar ajuda de profissionais que trabalham com segurança na Internet.</p>
        
      <p>Por outro lado, como usuário comuns, podemos adotar várias medidas preventivas, tais como manter ativos e atualizados os antivírus em nossos aparelhos que acessam à Internet, evitar fazer transações financeiras em redes abertas ou em computadores públicos e verificar os arquivos anexos das mensagens de estranhos, evitando baixá-los se não tiver certeza do seu conteúdo.</p>
      
